import math

sayı1 = float(input("Birinci Sayıyı Girin :\n"))
sayı2 = float(input("İkinci Sayıyı Girin :\n"))

print("İki Sayının Toplamı :\n",sayı1 + sayı2)
print("İki Sayının Farkı :\n", sayı1 - sayı2)
print("İki Sayının Çarpımı :\n",sayı1 * sayı2)
print("İki Sayının Bölümü :\n",sayı1 / sayı2)
print("Üst Alımı :\n",sayı1 ** sayı2)
print("Birinci Sayının Karekökü :\n",math.sqrt(sayı1))
print("İkinci Sayının Karekökü :\n",math.sqrt(sayı2))
